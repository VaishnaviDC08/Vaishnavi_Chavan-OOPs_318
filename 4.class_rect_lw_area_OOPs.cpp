#include<iostream>
using namespace std;
class Rectangle {
 private:
    int length;
    int width;
public:
    void display()
{
    cout<<"enter length: ";
    cin>> length;

    cout<<"enter width: ";
    cin>> width;
}

int area()
{
    return length * width ;
}
};
int main()
{
    Rectangle r;
    r.display();
    cout<<"area of rectangle: " << r.area();
    return 0;
}






